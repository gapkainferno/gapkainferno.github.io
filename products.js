// ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
(function() {
    if (typeof allProducts !== 'undefined') {
        Object.keys(allProducts).forEach(id => {
            const p = allProducts[id];
            
            if (p.category === 'seeds' || p.category === 'otherseeds') {
                
                // 1. Розрахунок ціни (завжди автоматично +25%)
                const isolatedPrice = Math.round(p.price * 1.25);
                
                p.seedVersions = {
                    openPollinated: {
                        label: "Open Pollinated",
                        price: p.price,
                        description: "Насіння вільного запилення.",
                        inStock: p.inStock
                    }
                };

                // Ізольовану версію додаємо тільки для тих товарів,
                // де ти явно вказала isolatedAvailable: true
                if (p.isolatedAvailable === true) {
                    p.seedVersions.isolated = {
                        label: "Ізольоване",
                        price: isolatedPrice,
                        description: "Контрольоване запилення. Гарантована чистота сорту 🛡️",
                        inStock: p.inStock
                    };
                }
            }
        });
        console.log("🔥 Homestead Inferno: Протоколи ціноутворення синхронізовано.");
    }
})();
